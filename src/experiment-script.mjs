import { createHash } from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";
import { formatNumber } from "./token-estimator.mjs";

export async function buildSparkompassExperimentScript(rootPath, options = {}) {
  const root = path.resolve(rootPath || ".");
  const planPath = resolveAgainstRoot(root, options.plan || options.planFile || "");
  if (!planPath) {
    throw new Error("Bitte Plan-Datei angeben: sparkompass experiment script . --plan evidence/experiment-plan.json");
  }
  const plan = JSON.parse(await fs.readFile(planPath, "utf8"));
  const outRelative = normalizeRelativePath(options.out || options.output || path.join(plan.evidence_dir || "evidence/codex-experiment", "run-experiment.sh"));
  const outPath = resolveAgainstRoot(root, outRelative);
  const script = buildScriptText({ root, plan, planPath, outPath });
  const gate = buildScriptGate(plan, script);

  return {
    schema: "SparkompassExperimentScriptV1",
    script_id: `experiment-script-${sha256(JSON.stringify({
      plan_id: plan.plan_id,
      out: outRelative,
      script_hash: sha256(script)
    })).slice(0, 12)}`,
    created_at: new Date().toISOString(),
    root,
    plan_file: planPath,
    relative_plan_file: relativeOrAbsolute(root, planPath),
    plan_id: plan.plan_id || "",
    plan_gate_status: plan.gate?.status || "unknown",
    output_file: outPath,
    relative_output_file: relativeOrAbsolute(root, outPath),
    script_sha256: `sha256:${sha256(script)}`,
    planned_runs: Array.isArray(plan.variants) ? plan.variants.length : 0,
    planned_task_outcomes: plan.commands?.planned_task_outcomes?.length || 0,
    commands: {
      preflight: plan.commands?.preflight?.length || 0,
      codex_runs: plan.commands?.planned_codex_runs?.length || 0,
      task_outcomes: plan.commands?.planned_task_outcomes?.length || 0,
      has_doctor_overhead: Boolean(plan.commands?.doctor_overhead),
      has_experiment_audit: Boolean(plan.commands?.experiment_audit),
      has_experiment_run: Boolean(plan.commands?.experiment_run),
      has_router_decide: Boolean(plan.commands?.router_decide)
    },
    run_order: buildRunOrder(plan),
    required_directories: requiredDirectories(root, plan, outPath).map((item) => relativeOrAbsolute(root, item)),
    gate,
    caveats: [
      "Dieses Skript fuehrt echte codex exec --json Befehle aus, wenn es gestartet wird; die Erzeugung des Skripts erzeugt keine offiziellen Usage-Werte.",
      "Der Plan bestimmt die Befehle. Aendere Plan, Prompts oder Konfiguration und erzeuge das Skript neu, bevor du Belege veroeffentlichst.",
      "Offizielle Usage entsteht erst in den geplanten JSONL-Dateien und muss danach mit experiment audit, experiment run und router decide geprueft werden."
    ],
    script
  };
}

export async function writeSparkompassExperimentScript(rootPath, artifact, options = {}) {
  const root = path.resolve(rootPath || ".");
  const outPath = resolveAgainstRoot(root, options.out || options.output || artifact.output_file || "");
  if (!outPath) return null;
  await fs.mkdir(path.dirname(outPath), { recursive: true });
  await fs.writeFile(outPath, artifact.script, { encoding: "utf8", mode: 0o755 });
  await fs.chmod(outPath, 0o755);
  return outPath;
}

export function formatSparkompassExperimentScript(script) {
  const problems = script.gate.reasons.length
    ? script.gate.reasons.map((reason) => `- ${reason}`).join("\n")
    : "- keine";
  return `
# SparkompassExperimentScriptV1

Gate: ${script.gate.status}
Plan: ${script.plan_id || "unbekannt"}

- Skript: ${script.relative_output_file}
- Geplante Codex-Runs: ${formatNumber(script.commands.codex_runs)}
- TaskOutcome-Befehle: ${formatNumber(script.commands.task_outcomes)}
- Preflight-Befehle: ${formatNumber(script.commands.preflight)}
- Experiment-Audit: ${script.commands.has_experiment_audit ? "ja" : "nein"}
- ExperimentRun: ${script.commands.has_experiment_run ? "ja" : "nein"}
- Router: ${script.commands.has_router_decide ? "ja" : "nein"}

## Gate-Probleme

${problems}

Hinweis: ${script.caveats.join(" ")}
`.trim();
}

function buildScriptText({ root, plan, planPath, outPath }) {
  const lines = [
    "#!/usr/bin/env bash",
    "set -euo pipefail",
    "",
    "# Generated by sparkompass experiment script. Do not edit by hand; update the plan and regenerate.",
    `cd ${shellQuote(root)}`,
    "",
    `echo ${shellQuote(`Sparkompass experiment plan: ${plan.plan_id || path.basename(planPath)}`)}`,
    `echo ${shellQuote("Official Usage will be written by codex exec --json runs; this script does not estimate billing.")}`,
    ""
  ];

  for (const directory of requiredDirectories(root, plan, outPath)) {
    lines.push(`mkdir -p ${shellQuote(relativeOrAbsolute(root, directory))}`);
  }
  lines.push("");

  for (const command of plan.commands?.preflight || []) {
    lines.push(`echo ${shellQuote(`preflight: ${command}`)}`);
    lines.push(command);
  }
  lines.push("");

  const runs = Array.isArray(plan.variants) ? plan.variants : [];
  for (let index = 0; index < runs.length; index += 1) {
    const run = runs[index];
    lines.push(`echo ${shellQuote(`codex ${index + 1}/${runs.length}: ${run.variant} r${run.repeat_index}`)}`);
    lines.push(run.codex_command?.shell || plan.commands?.planned_codex_runs?.[index] || "");
    lines.push("");
  }

  const taskCommands = plan.commands?.planned_task_outcomes || [];
  for (let index = 0; index < taskCommands.length; index += 1) {
    lines.push(`echo ${shellQuote(`task outcome ${index + 1}/${taskCommands.length}`)}`);
    lines.push(taskCommands[index]);
    lines.push("");
  }

  if (plan.commands?.doctor_overhead) {
    lines.push(`echo ${shellQuote("doctor overhead")}`);
    lines.push(plan.commands.doctor_overhead);
    lines.push("");
  }
  if (plan.commands?.experiment_audit) {
    lines.push(`echo ${shellQuote("experiment evidence audit")}`);
    lines.push(plan.commands.experiment_audit);
    lines.push("");
  }
  if (plan.commands?.experiment_run) {
    lines.push(`echo ${shellQuote("experiment run")}`);
    lines.push(plan.commands.experiment_run);
    lines.push("");
  }
  if (plan.commands?.router_decide) {
    lines.push(`echo ${shellQuote("router decide")}`);
    lines.push(plan.commands.router_decide);
    lines.push("");
  }
  lines.push(`echo ${shellQuote("Sparkompass experiment script finished.")}`);
  return `${lines.filter((line) => line !== null && line !== undefined).join("\n")}\n`;
}

function buildScriptGate(plan, script) {
  const reasons = [];
  if (plan.schema !== "SparkompassExperimentPlanV1") reasons.push("invalid-plan-schema");
  if (!plan.gate?.verified) reasons.push(`plan-not-verified:${plan.gate?.status || "unknown"}`);
  if (!Array.isArray(plan.variants) || plan.variants.length !== 12) reasons.push(`planned-run-count:${plan.variants?.length || 0}/12`);
  if (!plan.commands?.planned_codex_runs?.length) reasons.push("missing-planned-codex-runs");
  if (!plan.commands?.doctor_overhead) reasons.push("missing-doctor-overhead-command");
  if (!plan.commands?.experiment_audit) reasons.push("missing-experiment-audit-command");
  if (!plan.commands?.experiment_run) reasons.push("missing-experiment-run-command");
  if (!plan.commands?.router_decide) reasons.push("missing-router-command");
  if (!script.includes("set -euo pipefail")) reasons.push("missing-shell-safety-flags");
  if (!script.includes("experiment evidence audit")) reasons.push("audit-not-in-run-order");

  return {
    status: reasons.length ? "experiment-script-needs-review" : "verified-experiment-script",
    verified: reasons.length === 0,
    reasons,
    evidence_stage: reasons.length ? "script-needs-review" : "ready-for-official-codex-execution"
  };
}

function buildRunOrder(plan) {
  const runs = Array.isArray(plan.variants) ? plan.variants.map((run) => ({
    stage: "codex-run",
    variant: run.variant,
    repeat_index: run.repeat_index,
    usage_jsonl: run.expected_usage_jsonl
  })) : [];
  const taskOutcomes = Array.isArray(plan.variants) ? plan.variants
    .filter((run) => run.expected_task_outcome_json)
    .map((run) => ({
      stage: "task-outcome",
      variant: run.variant,
      repeat_index: run.repeat_index,
      task_outcome_json: run.expected_task_outcome_json
    })) : [];
  return [
    { stage: "preflight" },
    ...runs,
    ...taskOutcomes,
    { stage: "doctor-overhead" },
    { stage: "experiment-audit" },
    { stage: "experiment-run" },
    { stage: "router-decide" }
  ];
}

function requiredDirectories(root, plan, outPath) {
  const directories = new Set([path.dirname(outPath)]);
  for (const run of plan.variants || []) {
    if (run.expected_usage_jsonl) directories.add(path.dirname(resolveAgainstRoot(root, run.expected_usage_jsonl)));
    if (run.expected_task_outcome_json) directories.add(path.dirname(resolveAgainstRoot(root, run.expected_task_outcome_json)));
  }
  for (const commandPath of [
    plan.commands?.doctor_overhead,
    plan.commands?.experiment_audit,
    plan.commands?.experiment_run,
    plan.commands?.router_decide
  ]) {
    const out = extractRedirectPath(commandPath || "");
    if (out) directories.add(path.dirname(resolveAgainstRoot(root, out)));
  }
  return [...directories].sort();
}

function extractRedirectPath(command) {
  const match = String(command).match(/>\s+'([^']+)'/);
  return match ? match[1] : "";
}

function normalizeRelativePath(value) {
  const text = String(value || "").trim();
  if (!text) return "";
  return text.split(path.sep).join("/");
}

function resolveAgainstRoot(root, candidate) {
  if (!candidate) return "";
  return path.isAbsolute(candidate) ? candidate : path.resolve(root, candidate);
}

function relativeOrAbsolute(root, filePath) {
  const relative = path.relative(root, filePath);
  return relative && !relative.startsWith("..") && !path.isAbsolute(relative) ? relative.split(path.sep).join("/") : filePath;
}

function shellQuote(value) {
  return `'${String(value).replace(/'/g, "'\\''")}'`;
}

function sha256(value) {
  return createHash("sha256").update(value).digest("hex");
}
